main = do
    let str :: [Char]
        str = "hello"
    putStrLn str

    let c :: Char
        c = 'a'
    print $ c
    
    let i :: Int
        i = 1
    print $ i
    
    let d :: Double
        d = 1.2
    print $ d
    
    let b :: Bool
        b = True
    print $ b
    
    let arr :: [Int]
        arr = [1, 2, 3]
    print $ arr
